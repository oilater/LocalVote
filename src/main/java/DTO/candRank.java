package DTO;

public class candRank {
	String candno;
	String candname;
	String totalvote;

	public String getCandno() {
		return candno;
	}

	public void setCandno(String candno) {
		this.candno = candno;
	}

	public String getCandname() {
		return candname;
	}

	public void setCandname(String candname) {
		this.candname = candname;
	}

	public String getTotalvote() {
		return totalvote;
	}

	public void setTotalvote(String totalvote) {
		this.totalvote = totalvote;
	}

}


